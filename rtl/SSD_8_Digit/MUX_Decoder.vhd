LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;

ENTITY MUX_Decoder IS
PORT(
    ANODES : OUT STD_LOGIC_VECTOR(6 downto 0);
    INPUTS : IN STD_LOGIC_VECTOR(3 downto 0));
END MUX_Decoder;

--THIS WILL TAKE A 4-DIGIT INPUT FROM THE MUX AND BASED ON THAT OUTPUT A 7-DIGIT
--VALUE TO BE ASSIGNED TO THE SSD TO DISPLAY THE DESIRED "HEX" VALUE

ARCHITECTURE Behavioral OF MUX_Decoder IS
BEGIN
    
    ANODES <= "1000000" WHEN INPUTS = "0000" ELSE
              "1111001" WHEN INPUTS = "0001" ELSE 
              "0100100" WHEN INPUTS = "0010" ELSE -- 2
              "0110000" WHEN INPUTS = "0011" ELSE -- 3
              "0011001" WHEN INPUTS = "0100" ELSE -- 4
              "0010010" WHEN INPUTS = "0101" ELSE -- 5
              "0000010" WHEN INPUTS = "0110" ELSE -- 6
              "1111000" WHEN INPUTS = "0111" ELSE -- 7
              "0000000" WHEN INPUTS = "1000" ELSE -- 8 
              "0010000" WHEN INPUTS = "1001" ELSE -- 9
              "0001000" WHEN INPUTS = "1010" ELSE -- A
              "0000011" WHEN INPUTS = "1011" ELSE -- b
              "1000110" WHEN INPUTS = "1100" ELSE
              "0100001" WHEN INPUTS = "1101" ELSE 
              "0000110" WHEN INPUTS = "1110" ELSE 
              "0001110" WHEN INPUTS = "1111" ELSE
              "1111111";

END Behavioral;
